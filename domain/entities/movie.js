export class Movie {

    constructor(movieId, title, genre, duration, views, poster, overallRating, description) {

        this.movieId = movieId;
        this.title = title;
        this.genre = genre;
        this.duration = duration;
        this.views = views;
        this.poster = poster;
        this.overallRating = overallRating;
        this.description = description;
    }

}